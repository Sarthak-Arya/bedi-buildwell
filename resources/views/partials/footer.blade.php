<script>
    // WhatsApp widget functionality
    document.addEventListener('DOMContentLoaded', function() {
        const whatsappWidget = document.getElementById('whatsapp-widget');
        const whatsappBtn = document.getElementById('whatsapp-float');
        const chatBubble = document.getElementById('whatsapp-chat-bubble');
        const closeBtn = document.getElementById('whatsapp-close-btn');
        
        let hideTimeout;
        
        // Function to show chat bubble
        function showChatBubble() {
            if (chatBubble && window.innerWidth > 767) {
                chatBubble.style.display = 'block';
                setTimeout(function() {
                    chatBubble.classList.add('show');
                }, 10);
                
                // Auto-hide after 5 seconds
                clearTimeout(hideTimeout);
                hideTimeout = setTimeout(function() {
                    hideChatBubble();
                }, 5000);
            }
        }
        
        // Function to hide chat bubble
        function hideChatBubble() {
            if (chatBubble) {
                chatBubble.classList.remove('show');
                setTimeout(function() {
                    chatBubble.style.display = 'none';
                }, 300);
            }
            clearTimeout(hideTimeout);
        }
        
        // Toggle chat bubble on WhatsApp button click
        if (whatsappBtn) {
            whatsappBtn.addEventListener('click', function(e) {
                e.preventDefault();
                e.stopPropagation();
                
                if (chatBubble && window.innerWidth > 767) {
                    const isVisible = chatBubble.classList.contains('show') || chatBubble.style.display === 'block';
                    if (isVisible) {
                        hideChatBubble();
                    } else {
                        showChatBubble();
                    }
                }
            });
        }
        
        // Close button click handler
        if (closeBtn) {
            closeBtn.addEventListener('click', function(e) {
                e.preventDefault();
                e.stopPropagation();
                hideChatBubble();
            });
        }
        
        // Ensure widget is visible
        if (whatsappWidget) {
            whatsappWidget.style.display = 'block';
        }
        
        if (whatsappBtn) {
